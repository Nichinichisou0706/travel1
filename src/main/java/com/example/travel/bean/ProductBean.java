package com.example.travel.bean;

public class ProductBean {
    private int productID;
    private String productName;
    private String productDesc;
    private String productAuthor;
    private String productPicture;
    private String productPrice;
    private String productPhone;
    private String productRemain;
    private String productStatus;
    private String productDate;
    private String productStar;

    public int getProductID() {
        return productID;
    }

    public void setProductID(int productID) {
        this.productID = productID;
    }

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public String getProductDesc() {
        return productDesc;
    }

    public void setProductDesc(String productDesc) {
        this.productDesc = productDesc;
    }

    public String getProductAuthor() {
        return productAuthor;
    }

    public void setProductAuthor(String productAuthor) {
        this.productAuthor = productAuthor;
    }

    public String getProductPicture() {
        return productPicture;
    }

    public void setProductPicture(String productPicture) {
        this.productPicture = productPicture;
    }

    public String getProductPrice() {
        return productPrice;
    }

    public void setProductPrice(String productPrice) {
        this.productPrice = productPrice;
    }

    public String getProductPhone() {
        return productPhone;
    }

    public void setProductPhone(String productPhone) {
        this.productPhone = productPhone;
    }

    public String getProductRemain() {
        return productRemain;
    }

    public void setProductRemain(String productRemain) {
        this.productRemain = productRemain;
    }

    public String getProductStatus() {
        return productStatus;
    }

    public void setProductStatus(String productStatus) {
        this.productStatus = productStatus;
    }

    public String getProductDate() {
        return productDate;
    }

    public void setProductDate(String productDate) {
        this.productDate = productDate;
    }

    public String getProductStar() {
        return productStar;
    }

    public void setProductStar(String productStar) {
        this.productStar = productStar;
    }
}

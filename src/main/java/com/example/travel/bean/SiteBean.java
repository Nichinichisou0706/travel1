package com.example.travel.bean;

public class SiteBean {
    private int siteID;
    private String siteTitle;
    private String siteCity;
    private String siteStar;
    private String sitePrice;
    private String siteDesc;
    private String sitePicture;
    private String siteAuthor;

    public String getSiteTitle() {
        return siteTitle;
    }

    public void setSiteTitle(String siteTitle) {
        this.siteTitle = siteTitle;
    }

    public String getSiteAuthor() {
        return siteAuthor;
    }

    public void setSiteAuthor(String siteAuthor) {
        this.siteAuthor = siteAuthor;
    }

    public int getSiteID() {
        return siteID;
    }

    public void setSiteID(int siteID) {
        this.siteID = siteID;
    }

    public String getSiteCity() {
        return siteCity;
    }

    public void setSiteCity(String siteCity) {
        this.siteCity = siteCity;
    }

    public String getSiteStar() {
        return siteStar;
    }

    public void setSiteStar(String siteStar) {
        this.siteStar = siteStar;
    }

    public String getSitePrice() {
        return sitePrice;
    }

    public void setSitePrice(String sitePrice) {
        this.sitePrice = sitePrice;
    }

    public String getSiteDesc() {
        return siteDesc;
    }

    public void setSiteDesc(String siteDesc) {
        this.siteDesc = siteDesc;
    }

    public String getSitePicture() {
        return sitePicture;
    }

    public void setSitePicture(String sitePicture) {
        this.sitePicture = sitePicture;
    }
}
